package codeWars;

public class Metodos {

	
	
	
	public  int soma (int n1 , int n2){
		System.out.println(n1 + n2);
		return n1 + n2;
	}
	public  int subtracao (int n1, int n2) {
		System.out.println(n1-n2);
		return n1 - n2;
	}

	public  int multipicacao (int n1 , int n2){
		System.out.println(n1*n2);
		return n1 * n2;	

	}
	public  int divisao (int n1 , int n2){
		System.out.println(n1*n2);
		return n1 / n2;
	}
	}

